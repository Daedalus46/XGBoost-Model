# --- SIDEBAR INPUT FORM ---
st.sidebar.header("ðŸ”§ Input Vehicle Features")

# Unique values from CSV
unique_makes = sorted(df['Make'].dropna().unique().tolist())
Make = st.sidebar.selectbox("Make", unique_makes)

# Filter models based on selected Make
if Make:
    unique_models = sorted(df[df['Make'] == Make]['Model'].dropna().unique().tolist())
else:
    unique_models = []
Model = st.sidebar.selectbox("Model", unique_models)

unique_ev_types = sorted(df['EV_Type'].dropna().unique().tolist())
EV_Type = st.sidebar.selectbox("EV Type", unique_ev_types)

unique_cafv = sorted(df['CAFV_Eligibility'].dropna().unique().tolist())
CAFV_Eligibility = st.sidebar.selectbox("CAFV Eligibility", unique_cafv)

Vehicle_Age = st.sidebar.number_input("Vehicle Age (years)", min_value=0, max_value=30, value=3)

# Display implied Model Year based on current year (2025)
current_year = 2025
implied_model_year = current_year - Vehicle_Age
st.sidebar.text(f"Implied Model Year: {implied_model_year}")

unique_utilities = sorted(df['Electric_Utility'].dropna().unique().tolist())
Electric_Utility = st.sidebar.selectbox("Electric Utility Provider", unique_utilities)

predict_btn = st.sidebar.button("ðŸ”® Predict Electric Range")

# --- MAIN AREA ---
if predict_btn:
    input_df = pd.DataFrame({
        "Make": [Make],
        "Model": [Model],
        "EV_Type": [EV_Type],
        "CAFV_Eligibility": [CAFV_Eligibility],
        "Vehicle_Age": [Vehicle_Age],
        "Electric_Utility": [Electric_Utility]
    })
    try:
        pred = model.predict(input_df)[0]
        st.markdown(
            f"""
            <div class="result-box">
                <h2 style="color:#00eaff;">Estimated Electric Range</h2>
                <h1 style="font-size:60px; color:#00f7ff; text-shadow:0 0 30px #00f7ff;">
                    {pred:.2f} miles
                </h1>
            </div>
            """,
            unsafe_allow_html=True
        )
    except Exception as e:
        st.error(f"Prediction Error: {e}")
